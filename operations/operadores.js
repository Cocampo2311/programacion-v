// operadores aritméticos básicos
let counter = 100;

//1. suma
counter = counter + 20;
console.log(counter);
counter += 20;
console.log(counter);

//2. resta
counter = counter - 10;
console.log(counter);
counter -= 15;
console.log(counter);

//3.multiplicacion
counter = counter *2;
console.log(counter);
counter *= 3;
console.log(counter);

//4.division

counter = counter /3;
console.log(counter);
counter /= 2;
console.log(counter);

